package com.whilelooparray.assignment;

public class CharacterArrayDemo1 {

	public static void main(String[] args) {

		//Declare an Character Array
		char arr[]= {'A','E','I','O','U'};
		//Read Elements into an Character Array
		int i=0;
		while(i<arr.length)
		{
			System.out.println(arr[i]);
			i++;
		}

	}

}
