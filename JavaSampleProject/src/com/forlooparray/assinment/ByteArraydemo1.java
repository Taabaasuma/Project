package com.forlooparray.assinment;

public class ByteArraydemo1 {

	public static void main(String[] args) {
		
		//Declare an Byte Array
	    byte arr[]=new byte[5];
	    //Assign Element into an byte
	    arr[0]=25;
	    arr[1]=35;
	    arr[2]=45;
	    arr[3]=55;
	    arr[4]=65;
	    //Read Elements from an Byte array
	    for(int i=0;i<arr.length;i++)
	    {
	    	System.out.println(arr[i]);
	    }
	    
		
		

	}

}
