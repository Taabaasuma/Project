package com.dowhilelooparray.assignment;

public class CharacterArrayDemo1 {

	public static void main(String[] args) {

		//Declare an Character Array
		char arr[]= {'A','R','R','A','Y'};
		//Read ELements into an Character Array
		int i=0;
		do
		{
			System.out.println(arr[i]);
			i++;
		}while(i<arr.length);

	}

}
